import {
  Activity,
  AlarmSmoke,
  BadgeCheck,
  Bell,
  Bug,
  Cloud,
  Cpu,
  Database,
  Eye,
  FileLock2,
  Fingerprint,
  Globe,
  KeyRound,
  LayoutDashboard,
  Lock,
  Mail,
  Monitor,
  Network,
  Radar,
  Server,
  ServerCog,
  Shield,
  ShieldAlert,
  ShieldCheck,
  Smartphone,
  Wifi,
  Zap,
  type LucideIcon,
} from "lucide-react";

const ICONS: Record<string, LucideIcon> = {
  Activity,
  AlarmSmoke,
  BadgeCheck,
  Bell,
  Bug,
  Cloud,
  Cpu,
  Database,
  Eye,
  FileLock2,
  Fingerprint,
  Globe,
  KeyRound,
  LayoutDashboard,
  Lock,
  Mail,
  Monitor,
  Network,
  Radar,
  Server,
  ServerCog,
  Shield,
  ShieldAlert,
  ShieldCheck,
  Smartphone,
  Wifi,
  Zap,
};

/** Resolves a Lucide icon component from a stored icon name, falling back to ShieldCheck. */
export function getIcon(name?: string | null): LucideIcon {
  if (name && ICONS[name]) return ICONS[name];
  return ShieldCheck;
}
